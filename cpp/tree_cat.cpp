#include <iostream>
using namespace std;

// Main body of code
int main() {

   // Draws a tree
   cout << "   *" << endl;
   cout << "  ***" << endl;
   cout << " *****" << endl;
   cout << "*******" << endl;
   cout << "  ***" << endl;
   
   // Insert two blank lines
   cout << endl << endl;
   
   // Draws a cat
   cout << "/\\   /\\" << endl;
   cout << "  o o" << endl;
   cout << " =   =" << endl;
   cout << "  ---" << endl;

   // Exit main code
   return 0;
}
